"""
在python中用正则,要导入re模块
然后使用re中的功能去完成需求
"""

import re

#todo 验证手机号是否合法.规则:1开头,11位,纯数字
res = re.match('^1[0-9]{10}$','16677889900')
# $代表在此终止,不带的话一串数字只要前11位满足要求后面不管多长也会匹配上,不过结果也不会有就是 ^代表开头必须是1
print(res)

print(re.match('1[0-9]{10}','16655667788aaa')) # <re.Match object; span=(0, 11), match='16655667788'>
print(re.match('1[0-9]{10}','166556677aaa')) # None 正则规则不满足 返回none
print(re.match('^1[0-9]{10}$','166556677aaa')) # None 严格匹配开头和结尾
