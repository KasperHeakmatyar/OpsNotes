"""
变量：用于存储一个数据的容器，其值可以发生改变
语法：变量名 = 值 起名要见名知意
"""
a = 18
print(a)# 18
a = 19
print(a)# 19

"""
变量类型：
     1.整数/数字类型： int
     2.字符串类型： str
     3.布尔类型： bool
     4.小数（浮点型）： float
"""
age = 19
name = "Tom"
sex = False
height = 1.75

#查看变量的类型可以使用type()函数
print(type(age))
print(type(name))
print(type(sex))
print(type(height))

